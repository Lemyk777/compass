// Small "official source" link with an external-link icon. Opens in a new tab,
// rel="noopener" for safety. Used on the Italy/HK admission-odds cards to point
// students at the real, official admissions / Bando / scholarship pages.

export function OfficialSourceLink({
  href,
  label,
}: {
  href: string;
  label: string;
}) {
  return (
    <a
      href={href}
      target="_blank"
      rel="noopener noreferrer"
      className="inline-flex items-center gap-1 rounded text-[11px] font-medium text-accent transition-colors hover:text-ink focus-visible:focus-ring"
    >
      {label}
      <ExternalIcon />
    </a>
  );
}

function ExternalIcon() {
  return (
    <svg
      width="11"
      height="11"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2.2"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      className="shrink-0"
    >
      <path d="M15 3h6v6" />
      <path d="M10 14 21 3" />
      <path d="M18 13v6a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2V8a2 2 0 0 1 2-2h6" />
    </svg>
  );
}
