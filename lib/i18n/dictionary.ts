const DICT: Record<Lang, Dict> = { en, ru };

export type TFunc = (key: string) => string;

/** Pure translator factory — used by both server and client helpers. */
export function makeT(lang: Lang): TFunc {
  return (key: string) => DICT[lang][key] ?? DICT.en[key] ?? key;
}
